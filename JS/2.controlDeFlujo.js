// Control de flujo
//IF
let edad = 5

if (edad > 5 && edad < 15) {
    console.log("puedes jugar");
}else {
    console.log("no puedes jugar");
}

// While (Loop)
let = x =0
while (x <= 5) {
    console.log(x);
    x++
}

// Swich 

switch (2){
    case 1:
        console.log("caso 1");
        break;
    case 2:
        console.log("caso 2");
        break;
    case 3:
        console.log("caso 3");
        break;
    default:
        console.log("caso 4");
        break;
}

// For 



const numeros = [ 1,2,3,4,5]
for (let i = 0; i < numeros.length +1; i++) {
    console.log(i);
}